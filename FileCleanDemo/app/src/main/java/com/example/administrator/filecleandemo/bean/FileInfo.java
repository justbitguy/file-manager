package com.example.administrator.filecleandemo.bean;

public class FileInfo {

    public String path;
    public long size;

    public FileInfo(){}
    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }

}
